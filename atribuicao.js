const a = 7 // simbolo = tem função de atribuição
let b = 3

b += a // b = b + a (atribuição aditiva, acrescentando o valor de a na variável b)
console.log(b)

b -= 4 // b = b - 4
console.log(b)

b *= 2 // b = b * 2
console.log(b)

b /= 2 // b = b / 2
console.log(b)

b %= 2 // b = b % 2
console.log(b)